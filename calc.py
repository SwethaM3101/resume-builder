def add(x,y):
    return x+y
    
def sub(x,y):
    return x-y
    
def mul(x,y):
    return x*y
    
def div(x,y):
    return x/y
    
def mod(x,y):
    return x%y    
  
print("Simple Calculator")    
result=add(10,8)
print("Addition:",result)  

result=sub(10,8)
print("Subtraction:",result)

result=mul(10,8)
print("Multiplication:",result)

result=div(10,8)
print("Division:",result)
    
result=mod(10,8)
print("Modulo:",result)
